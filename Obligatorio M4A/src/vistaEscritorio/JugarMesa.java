    @Override
    public void mostrarEstadisticasjugador(ArrayList<String> estadisticas) {
        tblDatosMesa.removeAll();       
       
       
        DefaultTableModel datos = new DefaultTableModel();
        datos.addColumn("Ronda");
        datos.addColumn("Total Apostado");
        datos.addColumn("Ganado");
        datos.addColumn("Perdido");
        datos.addColumn("Balance");
        
        
        
        int numColumnas = 5;
        int numFilas = estadisticas.size() / numColumnas;
        datos.setRowCount(numFilas); 
        
       
        for (int fila = 0; fila < numFilas; fila++) {
        for (int columna = 0; columna < numColumnas; columna++) {
            int indice = fila * numColumnas + columna;
            
                datos.setValueAt(estadisticas.get(indice), fila, columna);
                        
            }
        }
        
        tblDatosMesa.setModel(datos);
    }

