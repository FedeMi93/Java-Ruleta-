    @Override
    public void mostrarValorOcurrencia(ArrayList<Integer> ocurrencia) {
       tableOcurrencia.removeAll();
       if(!ocurrencia.isEmpty()){
           mostrarUltimoNumero(ocurrencia.get(ocurrencia.size()-2)); 
       }
       
       
        DefaultTableModel datos = new DefaultTableModel();
        datos.addColumn("Valor");
        datos.addColumn("Ocurrencia");
        
        
        
        int numColumnas = 2;
        int numFilas = ocurrencia.size() / numColumnas;
        datos.setRowCount(numFilas); 
        
       
        for (int fila = 0; fila < numFilas; fila++) {
        for (int columna = 0; columna < numColumnas; columna++) {
            int indice = fila * numColumnas + columna;
            if(columna == 1){
                datos.setValueAt(ocurrencia.get(indice) + " %", fila, columna);
            } else{
                datos.setValueAt(ocurrencia.get(indice), fila, columna);
            }            
            }
        }
        
        tableOcurrencia.setModel(datos);
    }

