    @Override
    public void mostrarTiposApuesta(ArrayList<TipoApuesta> tiposDeApuesta) {
        this.tiposApuesta = tiposDeApuesta;
        ArrayList<String> listado = new ArrayList();
        for(TipoApuesta tp: tiposDeApuesta){
            listado.add(tp.getNombre());
        }
        listTiposApuesta.setListData(listado.toArray(new String[0]));
    }

    @Override
    public void iniciarMesa() {
        
        ArrayList<TipoApuesta> tp = new ArrayList();
        int[] v = listTiposApuesta.getSelectedIndices();
        for(int i: v){
            tp.add(this.tiposApuesta.get(i));
        }
        for(TipoApuesta tipo:tiposApuesta){
            if(tipo.isObligatorio()&& !tp.contains(tipo)){
                tp.add(tipo);
            }
        }
        controlador.IniciarMesa(tp);
        new OperarMesa(null,false,controlador.getElCroupier()).setVisible(true);
        this.setVisible(false);
    }

    private void salir() {
       controlador.logout();
    }
    
     

    
